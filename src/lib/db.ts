// ── Query helpers ───────────────────────────────────
export async function query<T>(sql: string, params: unknown[] = []): Promise<T[]> {
  await initDb();
  const result = await getClient().execute({ sql, args: params as Parameters<Client["execute"]>[0] extends { args: infer A } ? A : never });
  return result.rows as unknown as T[];
}

export async function queryOne<T>(sql: string, params: unknown[] = []): Promise<T | undefined> {
  await initDb();
  const result = await getClient().execute({ sql, args: params as Parameters<Client["execute"]>[0] extends { args: infer A } ? A : never });
  return (result.rows[0] as unknown as T) ?? undefined;
}

export async function execute(sql: string, params: unknown[] = []) {
  await initDb();
  const result = await getClient().execute({ sql, args: params as Parameters<Client["execute"]>[0] extends { args: infer A } ? A : never });
  return { lastInsertRowid: result.lastInsertRowid, changes: result.rowsAffected };
}
