import { createClient, type Client, type InStatement } from "@libsql/client";

// ── Client (singleton) ──────────────────────────────
let client: Client | null = null;
let initialized = false;

function getClient(): Client {
  if (!client) {
    client = createClient({
      url: process.env.TURSO_DATABASE_URL || "file:data/playbook.db",
      authToken: process.env.TURSO_AUTH_TOKEN,
    });
  }
  return client;
}

// ── Init ────────────────────────────────────────────
export async function initDb(): Promise<void> {
  if (initialized) return;
  const c = getClient();

  // Quick check: if core tables exist, skip DDL
  try {
    await c.execute("SELECT 1 FROM users LIMIT 0");
    await c.execute("SELECT 1 FROM business_classifications LIMIT 0");
    await runMigrations(c);
    initialized = true;
    // Seed any missing data
    await seedIfEmpty(c);
    return;
  } catch {
    // Tables don't exist yet — create everything
  }

  await createSchema(c);
  await runMigrations(c);
  await seedIfEmpty(c);
  initialized = true;
}

async function createSchema(c: Client): Promise<void> {
  await c.batch([
    `CREATE TABLE IF NOT EXISTS users (
      id INTEGER PRIMARY KEY,
      name TEXT NOT NULL,
      role TEXT NOT NULL CHECK(role IN ('staff', 'manager', 'unipoll', 'crew_lead')),
      category TEXT,
      password TEXT NOT NULL
    )`,
    `CREATE TABLE IF NOT EXISTS elections (
      id INTEGER PRIMARY KEY,
      name TEXT NOT NULL,
      election_date TEXT NOT NULL,
      status TEXT NOT NULL DEFAULT 'active'
    )`,
    `CREATE TABLE IF NOT EXISTS tasks (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      election_id INTEGER NOT NULL,
      title TEXT NOT NULL,
      category TEXT NOT NULL,
      status TEXT NOT NULL DEFAULT '未着手' CHECK(status IN ('未着手', '進行中', '確認待ち', '完了')),
      start_date TEXT,
      due_date TEXT,
      assignee_id INTEGER,
      sub_assignee_id INTEGER,
      playbook_conditions TEXT DEFAULT '[]',
      playbook_criteria TEXT DEFAULT '[]',
      playbook_pitfalls TEXT DEFAULT '[]',
      playbook_tip TEXT,
      memo TEXT,
      started_at TEXT,
      completed_at TEXT,
      effort_label TEXT,
      FOREIGN KEY (election_id) REFERENCES elections(id),
      FOREIGN KEY (assignee_id) REFERENCES users(id),
      FOREIGN KEY (sub_assignee_id) REFERENCES users(id)
    )`,
    `CREATE TABLE IF NOT EXISTS manuals (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      category TEXT NOT NULL,
      title TEXT NOT NULL,
      content TEXT NOT NULL
    )`,
    `CREATE TABLE IF NOT EXISTS call_categories (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      name TEXT NOT NULL,
      manual TEXT,
      sort_order INTEGER DEFAULT 0
    )`,
    `CREATE TABLE IF NOT EXISTS call_logs (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      category_id INTEGER NOT NULL,
      sub_category TEXT,
      duration TEXT NOT NULL,
      recorded_by INTEGER NOT NULL,
      recorded_at TEXT NOT NULL DEFAULT (datetime('now', 'localtime')),
      FOREIGN KEY (category_id) REFERENCES call_categories(id),
      FOREIGN KEY (recorded_by) REFERENCES users(id)
    )`,
    `CREATE TABLE IF NOT EXISTS kians (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      title TEXT NOT NULL,
      category TEXT NOT NULL,
      task_id INTEGER,
      due_timing TEXT,
      status TEXT NOT NULL DEFAULT '未着手' CHECK(status IN ('未着手', '起案済', '決裁済')),
      note TEXT,
      FOREIGN KEY (task_id) REFERENCES tasks(id)
    )`,
    `CREATE TABLE IF NOT EXISTS map_points (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      no INTEGER NOT NULL,
      name TEXT NOT NULL,
      address TEXT NOT NULL,
      lat REAL NOT NULL,
      lng REAL NOT NULL,
      voting_area TEXT,
      accessibility TEXT,
      type TEXT NOT NULL CHECK(type IN ('polling', 'poster', 'early')),
      city TEXT NOT NULL DEFAULT 'ichihara'
    )`,
    `CREATE TABLE IF NOT EXISTS crew_locations (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      map_point_id INTEGER,
      name TEXT NOT NULL,
      short_name TEXT NOT NULL,
      address TEXT NOT NULL,
      lat REAL NOT NULL,
      lng REAL NOT NULL,
      election_id INTEGER NOT NULL,
      open_date TEXT NOT NULL,
      close_date TEXT NOT NULL,
      open_time TEXT NOT NULL DEFAULT '08:30',
      close_time TEXT NOT NULL DEFAULT '20:00',
      FOREIGN KEY (map_point_id) REFERENCES map_points(id),
      FOREIGN KEY (election_id) REFERENCES elections(id)
    )`,
    `CREATE TABLE IF NOT EXISTS hourly_reports (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      crew_location_id INTEGER NOT NULL,
      report_date TEXT NOT NULL,
      time_slot TEXT NOT NULL,
      voter_count INTEGER NOT NULL,
      congestion_status TEXT NOT NULL CHECK(congestion_status IN ('空いている', 'やや混雑', '混雑')),
      operation_status TEXT NOT NULL CHECK(operation_status IN ('異常なし', '対応事案あり', '解決済')),
      note TEXT,
      note_tag TEXT CHECK(note_tag IN ('B', 'C', NULL)),
      reported_by INTEGER NOT NULL,
      reported_at TEXT NOT NULL DEFAULT (datetime('now', 'localtime')),
      FOREIGN KEY (crew_location_id) REFERENCES crew_locations(id),
      FOREIGN KEY (reported_by) REFERENCES users(id),
      UNIQUE(crew_location_id, report_date, time_slot)
    )`,
    `CREATE TABLE IF NOT EXISTS daily_reports (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      crew_location_id INTEGER NOT NULL,
      report_date TEXT NOT NULL,
      total_voters INTEGER NOT NULL,
      summary TEXT,
      handover TEXT,
      uploaded_by INTEGER NOT NULL,
      uploaded_at TEXT NOT NULL DEFAULT (datetime('now', 'localtime')),
      FOREIGN KEY (crew_location_id) REFERENCES crew_locations(id),
      FOREIGN KEY (uploaded_by) REFERENCES users(id),
      UNIQUE(crew_location_id, report_date)
    )`,
    `CREATE TABLE IF NOT EXISTS business_classifications (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      election_id INTEGER NOT NULL,
      category TEXT NOT NULL CHECK(category IN ('A', 'B', 'C')),
      title TEXT NOT NULL,
      description TEXT,
      sort_order INTEGER DEFAULT 0,
      FOREIGN KEY (election_id) REFERENCES elections(id)
    )`,
  ], "write");
}

async function runMigrations(c: Client): Promise<void> {
  // These may already exist from createSchema — safe to try
  try { await c.execute("SELECT start_date FROM tasks LIMIT 0"); } catch {
    await c.execute("ALTER TABLE tasks ADD COLUMN start_date TEXT");
  }
  try { await c.execute("SELECT sub_assignee_id FROM tasks LIMIT 0"); } catch {
    await c.execute("ALTER TABLE tasks ADD COLUMN sub_assignee_id INTEGER");
  }
}

// ── Seed ────────────────────────────────────────────
async function seedIfEmpty(c: Client): Promise<void> {
  // Core data
  const userCount = (await c.execute("SELECT COUNT(*) as count FROM users")).rows[0].count as number;
  if (userCount === 0) {
    await seedCoreData(c);
  }

  // start_date backfill
  const missingStart = (await c.execute("SELECT COUNT(*) as c FROM tasks WHERE start_date IS NULL")).rows[0].c as number;
  if (missingStart > 0) {
    await c.batch([
      "UPDATE tasks SET start_date = date(due_date, '-21 days') WHERE start_date IS NULL AND status = '完了'",
      "UPDATE tasks SET start_date = date(due_date, '-14 days') WHERE start_date IS NULL AND status = '進行中'",
      "UPDATE tasks SET start_date = date(due_date, '-7 days')  WHERE start_date IS NULL AND status = '確認待ち'",
      "UPDATE tasks SET start_date = date(due_date, '-10 days') WHERE start_date IS NULL AND status = '未着手'",
    ], "write");
  }

  // call_categories
  const callCatCount = (await c.execute("SELECT COUNT(*) as count FROM call_categories")).rows[0].count as number;
  if (callCatCount === 0) await seedCallCategories(c);

  // kians
  const kianCount = (await c.execute("SELECT COUNT(*) as count FROM kians")).rows[0].count as number;
  if (kianCount === 0) await seedKians(c);

  // call_logs
  const callLogCount = (await c.execute("SELECT COUNT(*) as count FROM call_logs")).rows[0].count as number;
  if (callLogCount === 0) await seedCallLogs(c);

  // map_points
  const mapCount = (await c.execute("SELECT COUNT(*) as count FROM map_points")).rows[0].count as number;
  if (mapCount === 0) await seedMapPoints(c);

  // crew users
  const crewUserCount = (await c.execute("SELECT COUNT(*) as count FROM users WHERE role = 'crew_lead'")).rows[0].count as number;
  if (crewUserCount === 0) await seedCrewUsers(c);

  // crew locations & reports
  const crewLocationCount = (await c.execute("SELECT COUNT(*) as count FROM crew_locations")).rows[0].count as number;
  if (crewLocationCount === 0) await seedCrewData(c);
}

